#ifndef _ABSTRACTFS_H_
#define _ABSTRACTFS_H_

#include <stdlib.h>

/* Module init/deinit */
void afs_init();
void afs_deinit();
/* Checks if the module is initialized and active */
int afs_initialized();
/* Mounts dir/archive to given mountpoint. Appends if append is zero, prepends otherwise */
void afs_mount(const char* dir, const char* mountpoint, int append);
/* Checks if given file can be found on the search path */
int afs_exists(const char* fname);
/* Retrieves filesize of given file */
long afs_file_length(const char* fname);
/* Reads buf_sz bytes from file into memory buffer. Buffer must have allocated the buf_sz bytes */
int afs_read_file_to_mem(const char* fname, unsigned char* buf, size_t buf_sz);

#endif /* ! _ABSTRACTFS_H_ */
